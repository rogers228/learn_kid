#程式的結束號
# 預設 一行不用結束號
# 也可以用結束號;
# 結束號; 通常用來做 下一行移到同一行
#---------------------
dog=53; cat=49; dc=dog+cat
print(f'小狗有{dog}隻,小貓有{cat}隻,總共有{dc}隻')

four=4; ft=53; foft=four*ft
print(f'小狗有{ft}隻,總共有{foft}隻腳')

four=4; fn=49; fofn=four*fn
print(f'小貓有{fn}隻,總共有{fofn}隻腳')

dfcf=foft+fofn
print(f'總共有{dfcf}隻腳')
